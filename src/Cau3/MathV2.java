package Cau3;

public class MathV2 {
    private MathV2(){

    }

    public static double add(double a, double b){
        return a + b;
    }

    public static double minus(double a, double b){
        return a - b;
    }
    public static double time(double a, double b){
        return a * b;
    }
    public static double div(double a, double b){
        return a / b;
    }
}
